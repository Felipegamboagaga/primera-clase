class personas():
    def __init__(self, nombre, peso, altura, pais_de_origen) -> None:
        self.name = nombre
        self.weight = peso
        self.height = altura
        self.nationality = pais_de_origen






Felipe = personas('Felipe', 70, 179, "Colombia")   
print(Felipe.nationality)
print(Felipe.height)
